import numpy as np
a = np.array([[12, 15, 20], 
              [25, 30, 37],
              [40, 62, 78]])
print("Min Number : ", np.min(a))   # min  : Min number function
print("Max Number :  ", np.max(a))  # max :  Maximum number funtion
print("Sum Number : ", np.sum(a))   # sum :  sum function
print("Standard Division  : ", np.std(a)) #std : Standard Division
print("Variance Number  : ", np.var(a))   #var :  variance Number

